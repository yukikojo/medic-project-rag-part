    def run_category_d(self) -> list[dict]:
        """端到端测试: 口语化输入 → 优化 → 检索 → LLM推荐"""
        if self.verbose:
            print("\n" + "=" * 65)
            print("  D类: 优化+LLM综合测试 (端到端)")
            print("=" * 65)

        if not self.pipeline:
            if self.verbose:
                print("  [SKIP] DeepSeek API 不可用, 跳过 D类测试")
            self.results["D_comprehensive"] = []
            self.results["summary"]["D"] = {"status": "skipped", "reason": "API不可用"}
            return []

        results = []

        for raw_query, category in COMPREHENSIVE_TEST_CASES:
            # 带优化
            start_opt = time.time()
            try:
                result_opt = self.pipeline.query(raw_query, top_k=5, optimize=True)
                latency_opt = (time.time() - start_opt) * 1000

                # 不带优化
                start_raw = time.time()
                result_raw = self.pipeline.query(raw_query, top_k=5, optimize=False)
                latency_raw = (time.time() - start_raw) * 1000

                opt_rec = result_opt.get("recommendation", {})
                raw_rec = result_raw.get("recommendation", {})

                # 解析 LLM 置信度百分比
                opt_conf = opt_rec.get("confidence", 0)
                if isinstance(opt_conf, str):
                    opt_conf = {"高": 85, "中": 60, "低": 35}.get(opt_conf, 50)
                raw_conf = raw_rec.get("confidence", 0)
                if isinstance(raw_conf, str):
                    raw_conf = {"高": 85, "中": 60, "低": 35}.get(raw_conf, 50)

                record = {
                    "raw_query": raw_query,
                    "category": category,
                    "optimized_query": result_opt.get("search_query", raw_query),
                    # 优化后结果
                    "opt_department": opt_rec.get("department", "N/A"),
                    "opt_disease": opt_rec.get("disease", "N/A"),
                    "opt_confidence": int(opt_conf),
                    "opt_tokens": opt_rec.get("usage", {}).get("total_tokens", 0),
                    "opt_latency_ms": round(latency_opt, 2),
                    # 未优化结果
                    "raw_department": raw_rec.get("department", "N/A"),
                    "raw_disease": raw_rec.get("disease", "N/A"),
                    "raw_confidence": int(raw_conf),
                    "raw_tokens": raw_rec.get("usage", {}).get("total_tokens", 0),
                    "raw_latency_ms": round(latency_raw, 2),
                    # 元数据
                    "opt_error": opt_rec.get("error"),
                    "raw_error": raw_rec.get("error"),
                }

                if self.verbose:
                    print(f"  [{category}] {raw_query[:25]:<25s}")
                    print(f"    优化后: {record['opt_department']:<12s} (conf={int(opt_conf)}%, "
                          f"{record['opt_tokens']}tok, {record['opt_latency_ms']:.0f}ms)")
                    print(f"    未优化: {record['raw_department']:<12s} (conf={int(raw_conf)}%, "
                          f"{record['raw_tokens']}tok, {record['raw_latency_ms']:.0f}ms)")

            except Exception as e:
                record = {
                    "raw_query": raw_query,
                    "category": category,
                    "error": str(e),
                }
                if self.verbose:
                    print(f"  [ERR] [{category}] {raw_query[:25]:<25s} ERROR: {e}")

            results.append(record)

        valid = [r for r in results if not r.get("error")]
        summary = {
            "total": len(results),
            "success": len(valid),
            "avg_opt_latency_ms": round(sum(r["opt_latency_ms"] for r in valid) / len(valid), 2) if valid else 0,
            "avg_raw_latency_ms": round(sum(r["raw_latency_ms"] for r in valid) / len(valid), 2) if valid else 0,
            "avg_opt_tokens": round(sum(r["opt_tokens"] for r in valid) / len(valid), 1) if valid else 0,
            "avg_raw_tokens": round(sum(r["raw_tokens"] for r in valid) / len(valid), 1) if valid else 0,
        }

        self.results["D_comprehensive"] = results
        self.results["summary"]["D"] = summary

        if self.verbose:
            print(f"\n  D类总结: 成功={summary['success']}/{summary['total']}, "
                  f"优化平均延迟={summary['avg_opt_latency_ms']:.0f}ms, "
                  f"未优化平均延迟={summary['avg_raw_latency_ms']:.0f}ms")

        return results

