def main():
    parser = argparse.ArgumentParser(
        description="RAG Medical — 分类测试框架 (本地模型 vs DeepSeek LLM)"
    )
    parser.add_argument(
        "--category", "-c",
        default="ABCD",
        help="测试类别: A=本地检索, B=DeepSeek LLM, C=查询优化, D=综合 (默认: 全部)"
    )
    parser.add_argument(
        "--output", "-o",
        default=None,
        help="输出目录 (默认: ../test_results/)"
    )
    parser.add_argument(
        "--chart",
        action="store_true",
        help="测试完成后自动生成图表"
    )
    parser.add_argument(
        "--chart-only",
        default=None,
        help="仅生成图表 (传入已有测试结果JSON路径)"
    )
    parser.add_argument(
        "--quiet", "-q",
        action="store_true",
        help="静默模式"
    )

    args = parser.parse_args()

    # 仅生成图表模式
    if args.chart_only:
        generate_charts_from_file(args.chart_only, args.output)
        return

    # 运行测试
    runner = TestRunner(output_dir=args.output, verbose=not args.quiet)
    runner.run_all(categories=args.category)
    runner.print_summary_table()

    # 生成图表
    if args.chart:
        result_path = runner._last_result_path
        generate_charts_from_file(result_path, args.output)


def generate_charts_from_file(json_path: str, output_dir: Optional[str] = None):
    """从JSON结果文件生成图表"""
    try:
        spec = importlib.util.spec_from_file_location(
            "chart_generator", os.path.join(_RAG_DIR, "chart_generator.py")
        )
        cg = importlib.util.module_from_spec(spec)
        spec.loader.exec_module(cg)
        generator = cg.ChartGenerator(output_dir=output_dir)
        generator.generate_all(json_path)
    except FileNotFoundError:
        print("[ERROR] chart_generator.py 未找到, 请先创建该模块")
    except Exception as e:
        print(f"[ERROR] 图表生成失败: {e}")
        import traceback
        traceback.print_exc()


if __name__ == "__main__":
    main()
