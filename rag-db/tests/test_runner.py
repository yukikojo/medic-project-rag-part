    def run_category_c(self) -> list[dict]:
        """测试优化前后的检索准确率对比"""
        if self.verbose:
            print("\n" + "=" * 65)
            print("  C类: 查询优化效果测试 (优化前后对比)")
            print("=" * 65)

        if not self.optimizer:
            if self.verbose:
                print("  [SKIP] QueryOptimizer 不可用, 跳过 C类测试")
            self.results["C_query_optimization"] = []
            self.results["summary"]["C"] = {"status": "skipped"}
            return []

        results = []

        for raw_query, expected_direction in OPTIMIZATION_TEST_CASES:
            # Step 1: 优化查询
            opt_start = time.time()
            opt_result = self.optimizer.optimize(raw_query)
            opt_latency = (time.time() - opt_start) * 1000

            optimized_query = opt_result.get("optimized_query", raw_query)
            symptoms = opt_result.get("symptoms", [])

            # Step 2: 用原始查询检索
            raw_start = time.time()
            raw_rag = self.vector_store.comprehensive_search(raw_query, top_k=5)
            raw_latency = (time.time() - raw_start) * 1000

            # Step 3: 用优化后查询检索
            opt_rag_start = time.time()
            opt_rag = self.vector_store.comprehensive_search(optimized_query, top_k=5)
            opt_rag_latency = (time.time() - opt_rag_start) * 1000

            raw_primary = raw_rag.get("primary_recommendation")
            opt_primary = opt_rag.get("primary_recommendation")

            raw_top1 = raw_rag["disease_results"][0] if raw_rag["disease_results"] else {}
            opt_top1 = opt_rag["disease_results"][0] if opt_rag["disease_results"] else {}

            # 获取置信度 (primary > top1_score)
            raw_confidence = raw_primary["confidence"] if raw_primary else raw_top1.get("score", 0)
            opt_confidence = opt_primary["confidence"] if opt_primary else opt_top1.get("score", 0)
            confidence_delta = opt_confidence - raw_confidence

            record = {
                "raw_query": raw_query,
                "optimized_query": optimized_query,
                "symptoms": symptoms,
                "optimization_method": opt_result.get("normalization_note", "N/A"),
                "opt_latency_ms": round(opt_latency, 2),
                # 原始查询的检索结果
                "raw_confidence": round(raw_confidence, 4),
                "raw_top_disease": raw_top1.get("disease", "N/A"),
                "raw_top_dept": raw_primary["department"] if raw_primary else "N/A",
                "raw_latency_ms": round(raw_latency, 2),
                # 优化后查询的检索结果
                "opt_confidence": round(opt_confidence, 4),
                "opt_top_disease": opt_top1.get("disease", "N/A"),
                "opt_top_dept": opt_primary["department"] if opt_primary else "N/A",
                "opt_rag_latency_ms": round(opt_rag_latency, 2),
                # 对比
                "confidence_delta": round(confidence_delta, 4),
                "improved": confidence_delta > 0.01,
                "total_latency_ms": round(opt_latency + opt_rag_latency, 2),
            }
            results.append(record)

            if self.verbose:
                arrow = "UP" if record["improved"] else ("--" if abs(confidence_delta) <= 0.01 else "DN")
                print(f"  [{arrow}] {raw_query[:20]:<20s} -> {optimized_query[:25]:<25s} "
                      f"(conf: {raw_confidence:.1%}->{opt_confidence:.1%}, delta={confidence_delta:+.1%})")

        # 汇总
        improved_count = sum(1 for r in results if r["improved"])
        deltas = [r["confidence_delta"] for r in results]
        avg_delta = sum(deltas) / len(deltas) if deltas else 0

        summary = {
            "total": len(results),
            "improved_count": improved_count,
            "improvement_rate": round(improved_count / len(results), 4) if results else 0,
            "avg_confidence_delta": round(avg_delta, 4),
            "avg_raw_confidence": round(sum(r["raw_confidence"] for r in results) / len(results), 4) if results else 0,
            "avg_opt_confidence": round(sum(r["opt_confidence"] for r in results) / len(results), 4) if results else 0,
            "avg_opt_latency_ms": round(sum(r["opt_latency_ms"] for r in results) / len(results), 2) if results else 0,
            "avg_total_latency_ms": round(sum(r["total_latency_ms"] for r in results) / len(results), 2) if results else 0,
        }

        self.results["C_query_optimization"] = results
        self.results["summary"]["C"] = summary

        if self.verbose:
            print(f"\n  C类总结: 优化提升率={summary['improvement_rate']:.1%}, "
                  f"平均置信度提升={summary['avg_confidence_delta']:+.1%}, "
                  f"({summary['avg_raw_confidence']:.1%} → {summary['avg_opt_confidence']:.1%})")

        return results

