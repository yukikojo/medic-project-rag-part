class TestRunner:
    """分类测试执行器 — 收集结构化指标"""

    def __init__(self, output_dir: Optional[str] = None, verbose: bool = True):
        self.verbose = verbose
        self.output_dir = output_dir or os.path.join(_PROJECT_DIR, "test_results")
        os.makedirs(self.output_dir, exist_ok=True)

        # 存储所有测试结果
        self.results = {
            "meta": {
                "timestamp": datetime.now().isoformat(),
                "test_version": "2.0",
                "categories": {},
            },
            "A_local_retrieval": [],
            "B_deepseek_llm": [],
            "C_query_optimization": [],
            "D_comprehensive": [],
            "summary": {},
        }

        # 延迟初始化的组件
        self._vector_store = None
        self._pipeline = None
        self._optimizer = None

    # ================================================================
    # 组件初始化
    # ================================================================

    @property
    def vector_store(self):
        if self._vector_store is None:
            spec = importlib.util.spec_from_file_location(
                "query_engine", os.path.join(_RAG_DIR, "query_engine.py")
            )
            qe = importlib.util.module_from_spec(spec)
            spec.loader.exec_module(qe)
            self._vector_store = qe.VectorStore()
        return self._vector_store

    @property
    def pipeline(self):
        if self._pipeline is None:
            try:
                spec = importlib.util.spec_from_file_location(
                    "deepseek_client", os.path.join(_RAG_DIR, "deepseek_client.py")
                )
                dc = importlib.util.module_from_spec(spec)
                spec.loader.exec_module(dc)
                self._pipeline = dc.RAGPipeline(verbose=False)
                self.results["meta"]["deepseek_available"] = True
            except Exception as e:
                if self.verbose:
                    print(f"  [INFO] DeepSeek 不可用: {e}")
                self.results["meta"]["deepseek_available"] = False
                self._pipeline = None
        return self._pipeline

    @property
    def optimizer(self):
        if self._optimizer is None:
            try:
                spec = importlib.util.spec_from_file_location(
                    "query_optimizer", os.path.join(_RAG_DIR, "query_optimizer.py")
                )
                qo = importlib.util.module_from_spec(spec)
                spec.loader.exec_module(qo)
                self._optimizer = qo.QueryOptimizer(mode="rule", verbose=False)
            except Exception as e:
                if self.verbose:
                    print(f"  [INFO] QueryOptimizer 不可用: {e}")
                self._optimizer = None
        return self._optimizer

    # ================================================================
    # 分类A: 本地向量检索测试
    # ================================================================

    def run_category_a(self) -> list[dict]:
        """测试 VectorStore 的检索准确率和性能"""
        if self.verbose:
            print("\n" + "=" * 65)
            print("  A类: 本地向量检索测试 (VectorStore)")
            print("=" * 65)

        results = []
        category_stats = defaultdict(lambda: {"count": 0, "correct": 0, "latencies": [], "scores": []})

        for query, expected_depts, category in LOCAL_TEST_CASES:
            start = time.time()
            rag_result = self.vector_store.comprehensive_search(query, top_k=5)
            latency_ms = (time.time() - start) * 1000

            primary = rag_result.get("primary_recommendation")
            top_disease = rag_result["disease_results"][0] if rag_result["disease_results"] else None

            # 判断是否正确: 推荐科室匹配预期
            # 当 primary 为 None 时, 使用 top-1 disease 的科室
            is_correct = False
            recommended_dept = ""
            confidence = 0.0

            if primary:
                recommended_dept = primary["department"]
                confidence = primary["confidence"]
                is_correct = any(
                    ed in recommended_dept or recommended_dept in ed
                    for ed in expected_depts
                )
            elif top_disease:
                # 回退: 用 top-1 疾病结果
                recommended_dept = top_disease.get("departments", "").split(", ")[0]
                confidence = top_disease.get("score", 0)
                is_correct = any(
                    ed in recommended_dept or recommended_dept in ed
                    for ed in expected_depts
                )

            record = {
                "query": query,
                "category": category,
                "expected_departments": expected_depts,
                "recommended_department": recommended_dept,
                "top_disease": top_disease["disease"] if top_disease else "N/A",
                "confidence": round(confidence, 4),
                "is_correct": is_correct,
                "latency_ms": round(latency_ms, 2),
                "top3_scores": [round(d["score"], 4) for d in rag_result["disease_results"][:3]],
            }
            results.append(record)

            # 分类统计
            stats = category_stats[category]
            stats["count"] += 1
            stats["latencies"].append(latency_ms)
            stats["scores"].append(confidence)
            if is_correct:
                stats["correct"] += 1

            if self.verbose:
                status = "PASS" if is_correct else "FAIL"
                print(f"  [{status}] [{category}] {query[:25]:<25s} -> {recommended_dept:<12s} "
                      f"(conf={confidence:.1%}, {latency_ms:.0f}ms)")

        # 汇总统计
        total = len(results)
        correct = sum(1 for r in results if r["is_correct"])
        latencies = [r["latency_ms"] for r in results]
        scores = [r["confidence"] for r in results if r["confidence"] > 0]

        summary = {
            "total": total,
            "correct": correct,
            "accuracy": round(correct / total, 4) if total > 0 else 0,
            "avg_latency_ms": round(sum(latencies) / len(latencies), 2) if latencies else 0,
            "min_latency_ms": round(min(latencies), 2) if latencies else 0,
            "max_latency_ms": round(max(latencies), 2) if latencies else 0,
            "avg_confidence": round(sum(scores) / len(scores), 4) if scores else 0,
            "by_category": {
                cat: {
                    "count": s["count"],
                    "correct": s["correct"],
                    "accuracy": round(s["correct"] / s["count"], 4) if s["count"] > 0 else 0,
                    "avg_latency_ms": round(sum(s["latencies"]) / len(s["latencies"]), 2) if s["latencies"] else 0,
                    "avg_confidence": round(sum(s["scores"]) / len(s["scores"]), 4) if s["scores"] else 0,
                }
                for cat, s in category_stats.items()
            },
        }

        self.results["A_local_retrieval"] = results
        self.results["summary"]["A"] = summary

        if self.verbose:
            print(f"\n  A类总结: 准确率={summary['accuracy']:.1%}, "
                  f"平均延迟={summary['avg_latency_ms']:.1f}ms, "
                  f"平均置信度={summary['avg_confidence']:.1%}")

        return results

